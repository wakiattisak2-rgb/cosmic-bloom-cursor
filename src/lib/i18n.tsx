type Key = keyof typeof STRINGS["en"];

interface I18nCtx {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (k: Key) => string;
}
const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");
  useEffect(() => {
    const saved = (typeof window !== "undefined" && (localStorage.getItem("aetros.locale") as Locale)) || null;
    if (saved === "en" || saved === "th") setLocaleState(saved);
  }, []);
  const setLocale = (l: Locale) => {
    setLocaleState(l);
    if (typeof window !== "undefined") localStorage.setItem("aetros.locale", l);
  };
  const t = (k: Key) => STRINGS[locale][k] ?? STRINGS.en[k] ?? k;
  return <Ctx.Provider value={{ locale, setLocale, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const v = useContext(Ctx);
  if (!v) throw new Error("useI18n outside provider");
  const tx = (en: string, th: string) => (v.locale === "th" ? th : en);
  return { ...v, tx };
}
